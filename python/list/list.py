# Lists: ordered, mutable, allows duplcate elements
# List is implemented in a dynamic-array way, is not an array(vector in cpp)

mylists = ["banana", "cherry", "apple"]
print(mylists)

mylists2 = [5, True, "apple", 3.1415] # allow different element types
print(mylists2)

mylists3 = [5, 5, 5, "apple", "apple"] # allow duplicates
print(mylists3)
